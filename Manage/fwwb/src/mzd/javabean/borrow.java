package mzd.javabean;

public class borrow {
	private String user_id;
	private String equitment_id;
	private String borrow_time;
	private String borrow_account;
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getEquitment_id() {
		return equitment_id;
	}
	public void setEquitment_id(String equitment_id) {
		this.equitment_id = equitment_id;
	}
	public String getBorrow_time() {
		return borrow_time;
	}
	public void setBorrow_time(String borrow_time) {
		this.borrow_time = borrow_time;
	}
	public String getBorrow_account() {
		return borrow_account;
	}
	public void setBorrow_account(String borrow_account) {
		this.borrow_account = borrow_account;
	}

}
