package mzd.javabean;

public class admin {
	private String admin_id;
	private String admin_name;
	private String admin_depart;
	private String admin_phone;
	private String admin_email;
	private String admin_warehouse;
	public String getAdmin_id() {
		return admin_id;
	}
	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}
	public String getAdmin_name() {
		return admin_name;
	}
	public void setAdmin_name(String admin_name) {
		this.admin_name = admin_name;
	}
	public String getAdmin_depart() {
		return admin_depart;
	}
	public void setAdmin_depart(String admin_depart) {
		this.admin_depart = admin_depart;
	}
	public String getAdmin_phone() {
		return admin_phone;
	}
	public void setAdmin_phone(String admin_phone) {
		this.admin_phone = admin_phone;
	}
	public String getAdmin_email() {
		return admin_email;
	}
	public void setAdmin_email(String admin_email) {
		this.admin_email = admin_email;
	}
	public String getAdmin_warehouse() {
		return admin_warehouse;
	}
	public void setAdmin_warehouse(String admin_warehouse) {
		this.admin_warehouse = admin_warehouse;
	}
	

}
